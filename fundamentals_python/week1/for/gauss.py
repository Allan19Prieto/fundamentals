# add all the numbers from 1 to 100 using a for loop

total = 0
for i in range(101):
    total += i
print(total)

print("")
# starting, stopping
for i in range(12, 16):
    print(i)

print("")
# starting, stopping, step
for i in range(0, 10, 2):
    print(i)

print("")
# negative step
for i in range(5, -1, -1):
    print(i)


